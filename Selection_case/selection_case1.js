let num = Math.floor(Math.random() * 10) % 6;
switch(num){
    case 0:
        console.log("Zero"); 
        break;
    case 1:   
        console.log("One");
        break;
    case 2:
        console.log("Two");
        break;
    case 3:
        console.log("Three");
        break;
    case 4:
        console.log("Four");
        break;
    case 5:
        console.log("Five");
        break;
    default:
        console.log("Please Enter the valid number");

}  